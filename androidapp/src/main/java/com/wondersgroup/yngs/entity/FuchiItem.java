public class FuchiItem {
    private String supportId;
    private String etpsName;
    private String regNo;
    private String uniScid;
    private String impspDate;

    public String getSupportId() {
        return supportId;
    }

    public void setSupportId(String supportId) {
        this.supportId = supportId;
    }

    public String getEtpsName() {
        return etpsName;
    }

    public void setEtpsName(String etpsName) {
        this.etpsName = etpsName;
    }

    public String getRegNo() {
        return regNo;
    }

    public void setRegNo(String regNo) {
        this.regNo = regNo;
    }

    public String getUniScid() {
        return uniScid;
    }

    public void setUniScid(String uniScid) {
        this.uniScid = uniScid;
    }

    public String getImpspDate() {
        return impspDate;
    }

    public void setImpspDate(String impspDate) {
        this.impspDate = impspDate;
    }
}
